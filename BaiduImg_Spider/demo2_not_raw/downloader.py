import multiprocessing
import random

from urllib.request import Request
from urllib.request import urlopen
import socket

from multiprocessing import Lock

threadLock = Lock()


class Downloader(object):
    def __init__(self):
        super().__init__()
        self.index = 1
        self.failure_num = 0
        self.url_queue = multiprocessing.Queue()
        self.index_queue = multiprocessing.Queue()
        socket.setdefaulttimeout(6)

    def download(self, pic_urls):
        while not pic_urls.empty():
            try:
                res = Request(pic_urls.get())
                res.add_header('User-Agent', self.random_headers())
                resp = urlopen(res,timeout=10)
                if str(resp.status) != '200':
                    print('未下载成功：')
                else:
                    index = self.index_queue.get()
                    with open(str(index) + '.jpg', 'wb') as f:
                        f.write(resp.read())
                        print('下载完成\n' + str(index))
            except Exception as e:
                print(e)


